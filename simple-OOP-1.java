// A simple class with a single method
class Dog {
     public void bark() {
       System.out.println("Woof!");
     }
   }
   
   // A main method that creates an instance of the Dog class and calls the bark method
   public class Main {
     public static void main(String[] args) {
       Dog myDog = new Dog();
       myDog.bark();
     }
   }
   